package Routenplaner;

public class SpecialPoint
{
	private int index;
	private double distance;

	public void setIndex(int index)
	{
		this.index = index;
		
	}

	public void setDistance(Double distance)
	{
		this.distance = distance;
		
	}

	public Double getDistance()
	{
		return distance;
	}

	public int getIndex()
	{
		return index;
	}

}
